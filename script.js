// Ticket Selection
document.querySelectorAll('.ticket-card .btn').forEach(btn => {
    btn.addEventListener('click', function() {
        const ticketType = this.closest('.ticket-card').querySelector('h3').textContent;
        
        // Add selection animation
        this.style.transform = 'scale(0.95)';
        setTimeout(() => {
            this.style.transform = 'scale(1)';
        }, 150);
        
        // Show selection feedback
        const originalText = this.textContent;
        this.textContent = 'Selected!';
        this.style.background = 'linear-gradient(135deg, #4ecdc4, #44a08d)';
        
        setTimeout(() => {
            this.textContent = originalText;
            this.style.background = '';
        }, 2000);
        
        console.log(`Selected ticket: ${ticketType}`);
    });
});

// Add loading animation to page
